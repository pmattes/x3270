/* Concatenate a set of arguments together. */
static char *
argcat(unsigned argc, const char **argv)
{
    varbuf_t r;
    char *sep = "";
    unsigned i;

    vb_init(&r);
    for (i = 0; i < argc; i++) {
	vb_appendf(&r, "%s%s", sep, argv[i]);
	sep = " ";
    }
    return vb_consume(&r);
}

